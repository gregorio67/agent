DROP TABLE HOST_INFO;


CREATE TABLE HOST_INFO
(
     HOST_NAME       	VARCHAR2(50) NOT NULL,
     IP_ADDR         	VARCHAR2(50) NOT NULL,     
     LOG_FILE_DIR    	VARCHAR2(50),
     APP_LOG_FILE    	VARCHAR2(50),
     ERR_LOG_FILE    	VARCHAR2(50),
     SQL_LOG_FILE    	VARCHAR2(50),
     CPU_USAGE       	NUMBER(5,2),
     MEM_USAGE       	NUMBER(5,2),
     USE_YN          	VARCHAR2(1),
     REG_DATE        	TIMESTAMP,
     REG_USER        	VARCHAR2(50)
)

DROP TABLE JVM_INFO

CREATE TABLE JVM_INFO
(
     HOST_NAME       	VARCHAR2(50) NOT NULL,
     JVM_NAME         	VARCHAR2(50) NOT NULL,     
     PROCESS_ID			NUMBER(10),
  	 HEAP_INIT		 	NUMBER(10),
  	 HEAP_MAX		 	NUMBER(10),
     HEAP_USAGE      	NUMBER(10),
     HEAP_FREE       	NUMBER(10),
     TOTAL_THREAD   	NUMBER(5),
     CURRENT_THREAD   	NUMBER(5),
     PEAK_THREAD   		NUMBER(5),
     USE_YN          	VARCHAR2(1),
     REG_DATE        	TIMESTAMP,
     REG_USER        	VARCHAR2(50)
)


