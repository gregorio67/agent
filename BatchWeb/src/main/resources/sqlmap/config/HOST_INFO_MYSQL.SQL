  ALTER TABLE HOST_INFO
  ADD CONSTRAINT PK_HOST_INFO PRIMARY KEY (HOST_NAME); 
  
  
  ALTER TABLE JVM_INFO
  ADD CONSTRAINT PK_JVM_INFO PRIMARY KEY (HOST_NAME, JVM_NAME);  
