DROP TABLE HOST_INFO;


CREATE TABLE HOST_INFO
(
     HOST_NAME       	VARCHAR(50) NOT NULL,
     IP_ADDR         	VARCHAR(50) NOT NULL,     
     LOG_FILE_DIR    	VARCHAR(50),
     APP_LOG_FILE    	VARCHAR(50),
     ERR_LOG_FILE    	VARCHAR(50),
     SQL_LOG_FILE    	VARCHAR(50),
     CPU_USAGE       	DECIMAL(5,2),
     MEM_USAGE       	DECIMAL(5,2),
     USE_YN          	VARCHAR(1),
     REG_DATE        	TIMESTAMP,
     REG_USER        	VARCHAR(50),
	 constraint HOST_INFO_UN unique (HOST_NAME, IP_ADDR)     
)
DROP TABLE JVM_INFO

CREATE TABLE JVM_INFO
(
     HOST_NAME       	VARCHAR(50) NOT NULL,
     JVM_NAME         	VARCHAR(50) NOT NULL,     
     PROCESS_ID			BIGINT(10),
  	 HEAP_INIT		 	BIGINT(10),
  	 HEAP_MAX		 	BIGINT(10),
     HEAP_USAGE      	BIGINT(10),
     HEAP_FREE       	BIGINT(10),
     TOTAL_THREAD   	BIGINT(5),
     CURRENT_THREAD   	BIGINT(5),
     PEAK_THREAD   		BIGINT(5),
     USE_YN          	VARCHAR(1),
     REG_DATE        	TIMESTAMP,
     REG_USER        	VARCHAR(50),
	 constraint HOST_INFO_UN unique (HOST_NAME)
)


 ALTER TABLE HOST_INFO
